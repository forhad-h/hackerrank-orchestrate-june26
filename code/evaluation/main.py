# ── Metrics ──────────────────────────────────────────────────────────────────


def _compute_metrics(
    predictions: List[Dict[str, Any]],
    ground_truth: List[Dict[str, str]],
) -> Dict[str, Any]:
    """Compare predictions against ground truth and compute metrics.

    ground_truth: list of dicts from sample_claims.csv with expected values.
    predictions: list of dicts from pipeline output.
    """
    gt_map = {row["user_id"]: row for row in ground_truth}

    correct_status = 0
    correct_evidence = 0
    correct_issue = 0
    correct_severity_exact = 0
    correct_severity_adjacent = 0
    correct_valid_image = 0
    jaccard_scores = []
    total = len(predictions)

    for pred in predictions:
        uid = pred.get("user_id", "")
        gt = gt_map.get(uid, {})
        if not gt:
            continue

        # claim_status
        if pred.get("claim_status", "") == gt.get("claim_status", ""):
            correct_status += 1

        # evidence_standard_met
        if pred.get("evidence_standard_met", "").lower() == gt.get("evidence_standard_met", "").lower():
            correct_evidence += 1

        # issue_type
        if pred.get("issue_type", "") == gt.get("issue_type", ""):
            correct_issue += 1

        # severity: exact + adjacent tolerance
        sev_order = ["none", "low", "medium", "high", "unknown"]
        pred_sev = pred.get("severity", "").lower()
        gt_sev = gt.get("severity", "").lower()
        if pred_sev == gt_sev:
            correct_severity_exact += 1
            correct_severity_adjacent += 1
        elif pred_sev in sev_order and gt_sev in sev_order:
            p_idx = sev_order.index(pred_sev)
            g_idx = sev_order.index(gt_sev)
            if abs(p_idx - g_idx) == 1 and "unknown" not in (pred_sev, gt_sev):
                correct_severity_adjacent += 1

        # valid_image
        if pred.get("valid_image", "").lower() == gt.get("valid_image", "").lower():
            correct_valid_image += 1

        # risk_flags: Jaccard similarity
        pred_flags = set(
            f.strip() for f in pred.get("risk_flags", "").split(";")
            if f.strip() and f.strip() != "none"
        )
        gt_flags = set(
            f.strip() for f in gt.get("risk_flags", "").split(";")
            if f.strip() and f.strip() != "none"
        )
        union = pred_flags | gt_flags
        if union:
            jaccard = len(pred_flags & gt_flags) / len(union)
        else:
            jaccard = 1.0  # both empty/none
        jaccard_scores.append(jaccard)

    return {
        "claim_status_accuracy": correct_status / max(total, 1),
        "evidence_standard_accuracy": correct_evidence / max(total, 1),
        "issue_type_accuracy": correct_issue / max(total, 1),
        "severity_accuracy_exact": correct_severity_exact / max(total, 1),
        "severity_accuracy_adjacent": correct_severity_adjacent / max(total, 1),
        "valid_image_accuracy": correct_valid_image / max(total, 1),
        "risk_flags_jaccard": sum(jaccard_scores) / max(len(jaccard_scores), 1),
        "total_rows": total,
    }


