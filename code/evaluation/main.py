def _load_claim_parser_prompt(variant: str, claim_object: str) -> str:
    """Load a claim parser prompt variant with object-specific enums."""
    from modules.claim_parser import _build_inline_system_prompt

    allowed_parts = OBJECT_PART_VALUES.get(claim_object, set())
    parts_list = "\n".join(f"  - {p}" for p in sorted(allowed_parts))
    issues_list = "\n".join(f"  - {t}" for t in sorted(ISSUE_TYPE_VALUES))

    try:
        text, _meta = load_prompt(variant, variables={
            "CLAIM_OBJECT": claim_object,
            "ISSUE_TYPES": issues_list,
            "OBJECT_PARTS": parts_list,
        })
        return text
    except (KeyError, FileNotFoundError):
        return _build_inline_system_prompt(claim_object)


def _load_vlm_prompt(variant: str, context: ClaimContext, parsed_claim: ParsedClaim) -> str:
    """Load a VLM prompt variant with claim context variables."""
    from modules.vlm_engine import _format_evidence_rules, _build_fallback_prompt

    evidence_text = _format_evidence_rules(context)
    secondary = ", ".join(parsed_claim.secondary_parts) if parsed_claim.secondary_parts else "none"

    try:
        text, _meta = load_prompt(variant, variables={
            "CLAIM_OBJECT": context.claim_object,
            "DAMAGE_DESCRIPTION": parsed_claim.damage_description or "No description provided.",
            "PRIMARY_ISSUE": parsed_claim.primary_issue_type or "unknown",
            "PRIMARY_PART": parsed_claim.primary_object_part or "unknown",
            "SECONDARY_PARTS": secondary,
            "IMAGE_COUNT": str(len(context.image_paths) if context.image_paths else 0),
            "EVIDENCE_RULES_TEXT": evidence_text,
        })
        return text
    except (KeyError, FileNotFoundError):
        return _build_fallback_prompt(context, parsed_claim)


def _run_single_config(
    contexts: List[ClaimContext],
    model_set: ModelSet,
    vlm_variant: str,
    parser_variant: str,
    label: str,
) -> Dict[str, Any]:
    """Run one model-set × prompt-variant configuration against sample claims.

    Returns aggregated metrics and operational stats.
    """
    vlm_model = model_set.get("vlm")
    text_model = model_set.get("text", "openai/gpt-4o-mini")

    results: List[Dict[str, Any]] = []
    total_calls = 0
    total_input_tokens = 0
    total_output_tokens = 0
    total_images = 0
    total_cost = 0.0
    start_time = time.time()
    errors = 0

    for ctx in contexts:
        try:
            # ── M2: Image Validation ──
            img_validation = validate_images(ctx)

            # ── M3: Claim Parser (with variant) ──
            parser_system = _load_claim_parser_prompt(parser_variant, ctx.claim_object)
            parser_user = _build_user_prompt(ctx.claim_object, ctx.user_claim)

            # Sanitize
            sanitized = sanitize_prompt(ctx.user_claim, context_id=ctx.user_id)
            conversation = sanitized.text if sanitized.text is not None else ctx.user_claim

            llm_result = call_llm(
                system_prompt=parser_system,
                user_prompt=_build_user_prompt(ctx.claim_object, conversation),
                model=text_model,
                model_set=model_set,
                response_format={"type": "json_object"},
                max_tokens=512,
                temperature=0.0,
                module_name=f"eval_parser_{label}",
            )
            total_calls += 1
            total_input_tokens += llm_result.usage.input_tokens
            total_output_tokens += llm_result.usage.output_tokens
            total_cost += llm_result.usage.cost_usd

            parsed_claim = _postprocess_parser_json(llm_result.content, ctx.claim_object, ctx.user_id)

            # ── M4: VLM Engine (with variant) ──
            vlm_system = _load_vlm_prompt(vlm_variant, ctx, parsed_claim)
            vlm_user = _build_vlm_user_prompt(
                ctx, parsed_claim,
                actual_image_count=len(img_validation.images_b64),
            )

            if img_validation.valid_image and img_validation.images_b64:
                vlm_result = call_llm(
                    system_prompt=vlm_system,
                    user_prompt=vlm_user,
                    model=vlm_model,
                    model_set=model_set,
                    response_format={"type": "json_object"},
                    max_tokens=1024,
                    temperature=0.0,
                    images_b64=img_validation.images_b64,
                    module_name=f"eval_vlm_{label}",
                )
                total_calls += 1
                total_input_tokens += vlm_result.usage.input_tokens
                total_output_tokens += vlm_result.usage.output_tokens
                total_cost += vlm_result.usage.cost_usd
                total_images += len(img_validation.images_b64)

                vlm_analysis = _postprocess_vlm_json(
                    vlm_result.content, ctx, img_validation,
                )
            else:
                vlm_analysis = _safe_default_vlm(ctx, img_validation)

            # ── M5: Evidence Evaluator ──
            evidence = evaluate_evidence(
                vlm_analysis, parsed_claim, ctx.evidence_rules,
                image_count=len(ctx.image_paths),
            )

            # ── M6: Risk Aggregator ──
            risk_flags = aggregate_risk_flags(
                vlm_analysis,
                parsed_claim,
                ctx.user_history,
            )

            # ── M7: Output Assembler ──
            output = assemble_output(
                ctx, vlm_analysis, parsed_claim,
                evidence, risk_flags,
            )

            results.append(output)

        except Exception as e:
            logger.error("Row %s failed: %s", ctx.user_id, e)
            errors += 1
            results.append({
                "user_id": ctx.user_id,
                "claim_status": "not_enough_information",
                "evidence_standard_met": "false",
                "issue_type": "unknown",
                "object_part": "unknown",
                "severity": "unknown",
                "valid_image": "false",
                "risk_flags": "manual_review_required",
                "supporting_image_ids": "none",
                "claim_status_justification": "Processing error",
                "evidence_standard_met_reason": "Processing error",
            })

    elapsed = time.time() - start_time

    return {
        "label": label,
        "model_set": model_set.name,
        "vlm_model": vlm_model,
        "text_model": text_model,
        "vlm_variant": vlm_variant,
        "parser_variant": parser_variant,
        "results": results,
        "errors": errors,
        "total_calls": total_calls,
        "total_input_tokens": total_input_tokens,
        "total_output_tokens": total_output_tokens,
        "total_images": total_images,
        "total_cost": total_cost,
        "elapsed_seconds": elapsed,
    }


def _postprocess_parser_json(content: str, claim_object: str, user_id: str) -> ParsedClaim:
    """Parse and coerce claim parser output."""
    import json
    from modules.claim_parser import _postprocess

    try:
        data = json.loads(content)
    except json.JSONDecodeError:
        parsed = extract_json_from_markdown(content)
        if parsed is not None:
            data = parsed
        else:
            return ParsedClaim(primary_issue_type="unknown", primary_object_part="unknown")
    return _postprocess(data, claim_object, user_id)


def _postprocess_vlm_json(content: str, context: ClaimContext, img_validation: ImageValidationResult) -> VLMAnalysis:
    """Parse and coerce VLM output."""
    import json
    from modules.vlm_engine import _postprocess_vlm

    try:
        data = json.loads(content)
    except json.JSONDecodeError:
        parsed = extract_json_from_markdown(content)
        if parsed is not None:
            data = parsed
        else:
            return VLMAnalysis(
                object_part="unknown",
                claim_status="not_enough_information",
                claim_status_justification="Could not parse VLM response.",
                supporting_image_ids="none",
                severity="unknown",
                valid_image=False,
            )
    return _postprocess_vlm(data, context, img_validation)


