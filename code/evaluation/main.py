def _find_best_configs(config_metrics: List[Tuple[Dict, Dict]]) -> Dict[str, Tuple[str, float]]:
    """Find the best config for each metric."""
    metrics_of_interest = [
        "claim_status_accuracy",
        "evidence_standard_accuracy",
        "issue_type_accuracy",
        "severity_accuracy_adjacent",
        "valid_image_accuracy",
        "risk_flags_jaccard",
    ]
    best: Dict[str, Tuple[str, float]] = {}
    for metric in metrics_of_interest:
        best_label = ""
        best_score = -1.0
        for result, metrics in config_metrics:
            score = metrics.get(metric, 0.0)
            if score > best_score:
                best_score = score
                best_label = f"{result['model_set']}/{result['vlm_variant'].split('/')[-1]}+{result['parser_variant'].split('/')[-1]}"
        best[metric] = (best_label, best_score)
    return best


def _select_best_config(config_metrics: List[Tuple[Dict, Dict]]) -> Dict:
    """Select the best overall config (highest average rank across metrics)."""
    metrics_of_interest = [
        "claim_status_accuracy",
        "evidence_standard_accuracy",
        "issue_type_accuracy",
        "severity_accuracy_adjacent",
        "valid_image_accuracy",
        "risk_flags_jaccard",
    ]

    best_result = config_metrics[0][0]
    best_avg = -1.0

    for result, metrics in config_metrics:
        scores = [metrics.get(m, 0.0) for m in metrics_of_interest]
        avg = sum(scores) / len(scores)
        if avg > best_avg:
            best_avg = avg
            best_result = result

    return best_result


# ── Ground Truth Loader ──────────────────────────────────────────────────────


def _load_ground_truth(path: Path) -> List[Dict[str, str]]:
    """Load sample_claims.csv ground truth."""
    rows = []
    with open(path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append(row)
    return rows


# ── Main ─────────────────────────────────────────────────────────────────────


def main():
    # Load .env so OpenRouter API key is available
    env_path = _CODE_ROOT / ".env"
    if env_path.is_file():
        load_dotenv(str(env_path))
        logger.info("Loaded environment from %s", env_path)
    elif not os.environ.get("OPENROUTER_API_KEY"):
        logger.warning("No .env found and OPENROUTER_API_KEY not set")

    parser = argparse.ArgumentParser(description="M8 — Evaluation Pipeline")
    parser.add_argument("--vlm-variant", choices=["analyze", "reasoning", "conservative", "all"],
                        default="all", help="VLM prompt variant to evaluate")
    parser.add_argument("--parser-variant", choices=["extract", "reasoning", "conservative", "all"],
                        default="all", help="Claim parser prompt variant to evaluate")
    parser.add_argument("--model-set", choices=["budget", "balanced", "premium", "all"],
                        default="all", help="Model set to evaluate")
    parser.add_argument("--output", default=str(REPORT_PATH),
                        help="Output report path")
    args = parser.parse_args()

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s [%(levelname)s] %(message)s",
    )

    # ── Load data ──
    logger.info("Loading sample claims from %s", SAMPLE_CLAIMS_PATH)
    loader = LocalCSVDataLoader(
        claims_path=SAMPLE_CLAIMS_PATH,
        user_history_path=USER_HISTORY_PATH,
        evidence_requirements_path=EVIDENCE_REQUIREMENTS_PATH,
    )
    contexts = loader.load()
    ground_truth = _load_ground_truth(SAMPLE_CLAIMS_PATH)
    logger.info("Loaded %d sample claims, %d ground truth rows", len(contexts), len(ground_truth))

    # ── Determine model sets to evaluate ──
    model_sets_to_run: List[ModelSet] = []
    if args.model_set == "all":
        model_sets_to_run = list(MODEL_SETS.values())
    else:
        model_sets_to_run = [MODEL_SETS[args.model_set]]

    # ── Determine prompt variants ──
    vlm_variants_to_run = VLM_PROMPT_VARIANTS if args.vlm_variant == "all" else [
        (f"vlm-engine/{args.vlm_variant}", f"VLM {args.vlm_variant}")
    ]
    parser_variants_to_run = CLAIM_PARSER_PROMPT_VARIANTS if args.parser_variant == "all" else [
        (f"claim-parser/{args.parser_variant}", f"Parser {args.parser_variant}")
    ]

    # ── Run all configs ──
    all_results: List[Dict[str, Any]] = []
    total_configs = len(model_sets_to_run) * len(vlm_variants_to_run) * len(parser_variants_to_run)
    config_idx = 0

    for model_set in model_sets_to_run:
        for vlm_pid, vlm_title in vlm_variants_to_run:
            for parser_pid, parser_title in parser_variants_to_run:
                config_idx += 1
                label = f"{model_set.name}/{vlm_pid.split('/')[-1]}+{parser_pid.split('/')[-1]}"
                logger.info(
                    "[%d/%d] Running %s — VLM: %s, Parser: %s, Model: %s",
                    config_idx, total_configs, label, vlm_title, parser_title, model_set.name,
                )

                token_tracker.reset()
                result = _run_single_config(
                    contexts, model_set,
                    vlm_variant=vlm_pid,
                    parser_variant=parser_pid,
                    label=label,
                )
                all_results.append(result)

                # Log per-config summary
                metrics = _compute_metrics(result["results"], ground_truth)
                logger.info(
                    "  → claim_status=%.1f%%  issue_type=%.1f%%  severity=%.1f%%  cost=$%.4f  time=%.1fs",
                    metrics["claim_status_accuracy"] * 100,
                    metrics["issue_type_accuracy"] * 100,
                    metrics["severity_accuracy_adjacent"] * 100,
                    result["total_cost"],
                    result["elapsed_seconds"],
                )

    # ── Generate report ──
    report = _generate_report(all_results, ground_truth)
    report_path = Path(args.output)
    report_path.write_text(report, encoding="utf-8")
    logger.info("Report written to %s", report_path)

    # Print summary to stdout
    print("\n" + "=" * 72)
    print("EVALUATION SUMMARY")
    print("=" * 72)
    for result in all_results:
        metrics = _compute_metrics(result["results"], ground_truth)
        print(f"\n{result['label']}:")
        print(f"  claim_status acc:  {metrics['claim_status_accuracy']:.1%}")
        print(f"  issue_type acc:    {metrics['issue_type_accuracy']:.1%}")
        print(f"  severity (adj.):   {metrics['severity_accuracy_adjacent']:.1%}")
        print(f"  risk_flags Jaccard:{metrics['risk_flags_jaccard']:.2f}")
        print(f"  cost:              ${result['total_cost']:.4f}")
        print(f"  time:              {result['elapsed_seconds']:.1f}s")

    # ── Security summary ─────────────────────────────────────────────────────
    log_security_summary()


if __name__ == "__main__":
    main()
